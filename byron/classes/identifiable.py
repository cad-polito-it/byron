from abc import ABC, abstractmethod


class IdentifiableABC(ABC):
    @property
    @abstractmethod
    def _identity(self):
        pass

    def __hash__(self):
        return hash(self._identity)

    def __eq__(self, other):
        if other is None or not isinstance(other, self.__class__):
            return False
        else:
            return self._identity == other._identity
